public class StringAPIDemoA09{
	public static void main(String[] args){
		String str = "###mengzhaoyang";
		if(str.startsWith("##"))
			System.out.println("str start with ##");
		else
			System.out.println("str not start with ##");
		if(str.endsWith("##"))
			System.out.println("str end with ##");
		else 
			System.out.println("str not end with ##");
	}
}
